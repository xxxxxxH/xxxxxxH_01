public class SpaceList {

    private String img;
    private int war_number;
    private String property;
    private String sale_form;
    private String stype;

    private int bid;

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getWar_number() {
        return war_number;
    }

    public void setWar_number(int war_number) {
        this.war_number = war_number;
    }

    public String getProperty() {
        return property;
    }

    public void setProperty(String property) {
        this.property = property;
    }

    public String getSale_form() {
        return sale_form;
    }

    public void setSale_form(String sale_form) {
        this.sale_form = sale_form;
    }

    public String getStype() {
        return stype;
    }

    public void setStype(String stype) {
        this.stype = stype;
    }

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }


}
